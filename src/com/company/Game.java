    private int[][] maze;
    public static ArrayList<Enemy> enemies;
    private Timer timer = new Timer(1000, this);
    private GamePanel gamePanel;
    private static JLabel hp;
    private static JLabel enemiesLeft;
    private static JLabel coinsLeft;
    private boolean isWinner;
    private static int level;
    private GamePlayer player;
    private static int nrOfCoins = 0;
    private static String auxName;


    public Game(int level) throws InterruptedException {

        this.maze = maze3d[level - 1];
        Game.level = level;
        enemies = new ArrayList<>();

        generatePlayerAndEnemies();

        if (level == 1) {
            StartFrame startFrame = new StartFrame(player);
            nrOfCoins = 2;
            while (!startFrame.isButtonPressed()) System.out.print("");
        } else {
            player.setName(auxName);
            nrOfCoins = 3;
        }

        JFrame gameFrame = new JFrame("SwordsAndJava");
        gameFrame.setIconImage((new ImageIcon("Sword.JFIF")).getImage());
        int width = 660;
        int height = 660;
        gameFrame.setSize(width, height);
        gameFrame.setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
        gameFrame.setResizable(false);

        hp = new JLabel(String.valueOf("Hp left: " + player.getHp()), SwingConstants.LEFT);
        hp.setForeground(Color.MAGENTA);
        hp.setFont(new Font("Times New Roman", Font.PLAIN, 25));
        hp.setSize(200, 30);

        enemiesLeft = new JLabel("Enemies left: " + enemies.size(), SwingConstants.LEFT);
        enemiesLeft.setForeground(Color.MAGENTA);
        enemiesLeft.setFont(new Font("Times New Roman", Font.PLAIN, 25));
        enemiesLeft.setBounds(0, 40, 200, 30);

        coinsLeft = new JLabel("Coins left: " + nrOfCoins, SwingConstants.LEFT);
        coinsLeft.setForeground(Color.MAGENTA);
        coinsLeft.setFont(new Font("Times New Roman", Font.PLAIN, 25));
        coinsLeft.setBounds(0, 77, 200, 30);

        gamePanel = new GamePanel(maze, width, height, player);
        gamePanel.setLayout(null);
        gamePanel.add(hp);
        gamePanel.add(enemiesLeft);
        gamePanel.add(coinsLeft);
        gameFrame.addKeyListener(gamePanel);
        gameFrame.addMouseListener(gamePanel);
        gameFrame.add(gamePanel);
        gameFrame.setVisible(true);
        boolean game = true;
        timer.start();
        
        while (game) {

            //gameFrame.add(gamePanel);
            //gameFrame.setVisible(true);
            //timer.start();
            if (player.getHp() <= 0) {
                game = false;
                maze[player.getRow()][player.getCol()] = 0;
                timer.stop();
                gameFrame.removeKeyListener(gamePanel);
                gameFrame.removeMouseListener(gamePanel);
                isWinner = false;
            } else if (enemies.size() == 0 && nrOfCoins == 0) {
                game = false;
                timer.stop();
                gameFrame.removeKeyListener(gamePanel);
                gameFrame.removeMouseListener(gamePanel);
                isWinner = true;
            }
        }

        IntermediatePanel intermediatePanel = new IntermediatePanel(isWinner, level);
        gameFrame.getContentPane().remove(gamePanel);
        gameFrame.add(intermediatePanel);
        gameFrame.revalidate();

        while (!intermediatePanel.isExitPressed() && !intermediatePanel.isRestartPressed() && !intermediatePanel.isNextLevelPressed())
            System.out.print("");

        if (intermediatePanel.isExitPressed())
            System.exit(0);
        else if (intermediatePanel.isRestartPressed()) {

            gameFrame.setVisible(false);
            new Game(1);
        } else {
            gameFrame.setVisible(false);
            new Game(2);
        }
    }

    public void generatePlayerAndEnemies() {
        player = new GamePlayer("", 6, 7, maze);
        int noOfEnemies = 5 + level - 1;
        if (level == 1) {
            Enemy enemy1 = new Enemy(5, 22, maze, player);
            Enemy enemy2 = new Enemy(7, 16, maze, player);
            Enemy enemy3 = new Enemy(15, 16, maze, player);
            Enemy enemy4 = new Enemy(20, 10, maze, player);

            enemies.add(enemy1);
            enemies.add(enemy2);
            enemies.add(enemy3);
            enemies.add(enemy4);

        }
        else {
            Enemy enemy1 = new Enemy(5, 23, maze, player);
            Enemy enemy2 = new Enemy(9, 16, maze, player);
            Enemy enemy3 = new Enemy(15, 16, maze, player);
            Enemy enemy4 = new Enemy(17, 11, maze, player);
            Enemy enemy5 = new Enemy(11, 21, maze, player);
            Enemy enemy6 = new Enemy(18, 15, maze, player);
            Enemy enemy7 = new Enemy(22, 23, maze, player);


            enemies.add(enemy1);
            enemies.add(enemy2);
            enemies.add(enemy3);
            enemies.add(enemy4);
            enemies.add(enemy5);
            enemies.add(enemy6);
            enemies.add(enemy7);
        }
    }

    @Override
    public void actionPerformed(ActionEvent e) { //move Enemies
        for (Enemy enemy : enemies)
            enemy.move();

        Enemy.playerHasMoved = false;
        gamePanel.repaint();
    }

    public static void setHp(String hp) {
        Game.hp.setText("Hp left: " + hp);
    }

    public static int getNrOfCoins() {
        return nrOfCoins;
    }

    public static void setNrOfCoins(int nrOfCoins) {
        Game.nrOfCoins = nrOfCoins;
        setCoinsLeft();
    }

    public static void setEnemiesLeft() {
        enemiesLeft.setText("Enemies left: " + enemies.size());
    }

    public static void setCoinsLeft() {
        coinsLeft.setText("Coins left: " + nrOfCoins);
    }

    public static void setAuxName(String string) {
        auxName = string;
    }
